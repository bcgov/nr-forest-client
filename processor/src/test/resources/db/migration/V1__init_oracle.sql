-- Adding Triggers to allow fornew clients to have auto generated clientids
CREATE OR REPLACE TRIGGER THE.client_number_trigger
BEFORE INSERT ON THE.FOREST_CLIENT
FOR EACH ROW
BEGIN
    SELECT LPAD(TO_CHAR(client_number_seq.NEXTVAL), 8, '0')
    INTO :NEW.CLIENT_NUMBER
    FROM dual;
END;

CREATE OR REPLACE TRIGGER THE.client_contact_id_trigger
BEFORE INSERT ON THE.CLIENT_CONTACT
FOR EACH ROW
BEGIN
    SELECT client_contact_seq.NEXTVAL
    INTO :NEW.CLIENT_CONTACT_ID
    FROM dual;
END;


